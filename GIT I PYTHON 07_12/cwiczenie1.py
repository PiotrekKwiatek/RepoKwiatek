# dane od uzytkownika
number1 = float(input("Podaj pierwszą liczbę"))
number2 = float(input("Podaj drugą liczbę"))

# operacja matematyczna
add = number1 + number2
iloczyn = number1 * number2
potęgowanie = number1 ** number2

#wyswietlanie wyniku

print(f"Suma Twoich liczb {number1} oraz {number2} to {add}")
print(f"Iloczyn Twoich liczb {number1} oraz {number2} to {iloczyn}")
print(f"Potęga Twoich liczb {number1} oraz {number2} to {potęgowanie}")

